import numpy as np
import matplotlib.pyplot as plt

# Constants (given)
flue_gas_mass_flow = 5.7155       # kg/s
flue_gas_inlet_temp = 389.05      # °C
flue_gas_outlet_temp = 200        # °C
cpg = 1131.62                    # J/kg·K

water_inlet_temp = 50             # °C
cpw = 4186                       # J/kg·K
overall_UA = 6461.35             # W/K
LMTD = 203.76                   # °C
economy_efficiency = 0.7          # Fraction (70%)

# Steam parameters
# Latent heat of vaporization of steam (approximate)
latent_heat_steam = 2257000       # J/kg (at 100°C)

# Simulation range for water flow rate (kg/s)
water_flow_rates = np.linspace(1, 5, 50)  # from 1 to 5 kg/s

# Arrays to store results
feed_water_temps = []
steam_loads = []
heat_absorbed = []
temp_diffs = []

for m_water in water_flow_rates:
    # Step 1: Heat available from flue gas
    delta_T_gas = flue_gas_inlet_temp - flue_gas_outlet_temp
    Q_gas = flue_gas_mass_flow * cpg * delta_T_gas  # Total heat from flue gas (W)

    # Step 2: Heat absorbed by water without efficiency loss (max possible)
    # Assuming water outlet temp = inlet temp + Q / (m * cp)
    # Let's calculate heat absorbed based on UA and LMTD (max heat transfer)
    Q_UA = overall_UA * LMTD

    # Effective heat absorbed by water (considering economy efficiency and Q_UA)
    Q_effective = min(Q_gas, Q_UA) * economy_efficiency

    # Step 3: Calculate new feed water outlet temperature
    delta_T_water = Q_effective / (m_water * cpw)
    water_outlet_temp = water_inlet_temp + delta_T_water

    # Step 4: Steam load calculation
    # Steam load = Heat absorbed / latent heat (kg/s of steam produced)
    steam_load = Q_effective / latent_heat_steam

    # Store results
    feed_water_temps.append(water_outlet_temp)
    steam_loads.append(steam_load)
    heat_absorbed.append(Q_effective)
    temp_diffs.append(delta_T_water)

