"""Economizer Simulation"""

import numpy as np
import matplotlib.pyplot as plt

# Given Data from Economizer
# Heat transfer input
UA = 6461.348722            # W/K
mass_flow_gas = 5.71551822  # kg/s
mass_flow_water = 2.777777778  # kg/s
cp_gas = 1131.62145         # J/kg-K
cp_water = 4186             # J/kg-K

# Inlet temperatures
T_gas_in = 389.0484048      # °C
T_water_in = 50.0           # °C

# Initial assumptions
T_gas_out = 200.0           # °C (initial)
T_water_out = 163.2261779   # °C (initial)
iterations = 20

# Lists to store iteration history
T_gas_out_list = [T_gas_out]
T_water_out_list = [T_water_out]

# Iterative heat exchange calculation
for i in range(iterations):
    # Mean temp difference
    delta_T1 = T_gas_in - T_water_out
    delta_T2 = T_gas_out - T_water_in
    if delta_T1 == delta_T2:
        LMTD = delta_T1
    else:
        LMTD = (delta_T1 - delta_T2) / np.log(delta_T1 / delta_T2)

    # Heat transferred (Q = UA * LMTD)
    Q = UA * LMTD  # in watts

    # Update outlet temperatures
    T_gas_out = T_gas_in - Q / (mass_flow_gas * cp_gas)
    T_water_out = T_water_in + Q / (mass_flow_water * cp_water)

    # Save for plotting
    T_gas_out_list.append(T_gas_out)
    T_water_out_list.append(T_water_out)

    # Optional: print every few iterations
    print(f"Iteration {i+1}: Gas Out = {T_gas_out:.6f} °C, Water Out = {T_water_out:.6f} °C")

# Final results
print(f"\n  Final Flue Gas Outlet Temperature: {T_gas_out:.6f} °C")
print(f"Final Water Outlet Temperature: {T_water_out:.6f} °C")

