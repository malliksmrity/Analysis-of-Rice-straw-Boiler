"""Settling Chamber Simulation"""

import numpy as np
import matplotlib.pyplot as plt

# -------------------------------
# Input data (from spreadsheet)
flue_gas_flow_kg_s = 5.71551822        # kg/s
Cp = 1310.479888                       # J/kg-K
air_inlet_temp = 861.5752447          # °C (initial gas temp entering chamber)
initial_T_out = 700                   # °C (assumed gas outlet temp to convection bank)
mean_temp = 780.7876223               # °C
wall_temp = 210                       # °C
K = 0.247167463                       # W/m-K
L = 1.693436293                       # m

# Geometric & surface properties
surface_area = 25.9                   # m²
refractory_area = 6.4                 # m²
emissivity = 0.307807109              # Dimensionless
sigma = 5.67e-8                       # W/m²-K⁴

# -------------------------------
# Iterative calculation
iterations = 13
T_out = initial_T_out
T_out_list = [T_out]

for i in range(iterations):
    # Convert temperatures to Kelvin
    T_out_K = T_out + 273.15
    T_wall_K = wall_temp + 273.15

    # Conduction loss
    Q_cond = K * surface_area * (mean_temp - wall_temp) / L

    # Radiation loss
    Q_rad = emissivity * sigma * refractory_area * (T_out_K**4 - T_wall_K**4)

    # Total heat loss
    Q_total_loss = Q_cond + Q_rad

    # Net heat carried by gas = mass flow * Cp * (T_out - air_inlet)
    Q_net = -Q_total_loss  # since losses reduce the energy
    T_out_new = Q_net / (flue_gas_flow_kg_s * Cp) + air_inlet_temp
    T_out_list.append(T_out_new)

    print(f"Iteration {i+1}: Outlet Gas Temp = {T_out_new:.4f} °C")

    T_out = T_out_new  # update for next iteration

# -------------------------------
# Final output
final_T_out = round(T_out, 4)
